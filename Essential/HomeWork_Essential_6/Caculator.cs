using System;
using System.Collections.Generic;
using System.Text;

namespace HomeWork_Essential_6
{
	static class Caculator
	{
		public static double Add(int term1, int term2) {
			return term1 + term2;
		}
		public static double Sub(int term1, int term2) {
			return term1 - term2;
		}
		public static double Mul(int term1, int term2) {
			return term1 * term2;
		}
		public static double Div(int term1, int term2) {
			return term1 / term2;
		}

	}
}
